// TestQBXVersionBytes verifies QBX uses Dilithium-derived version bytes,
// NOT Bitcoin's 0x00/0x05.
func TestQBXVersionBytes(t *testing.T) {
	c := NewQBXCoin()

	// QBX P2PKH = 0x32 (M...), NOT Bitcoin's 0x00 (1...)
	if c.P2PKHVersionByte() != 0x32 {
		t.Errorf("expected P2PKH version 0x32, got 0x%02x — QBX uses M... not 1... addresses", c.P2PKHVersionByte())
	}

	// QBX P2SH = 0x37 (P...), NOT Bitcoin's 0x05 (3...)
	if c.P2SHVersionByte() != 0x37 {
		t.Errorf("expected P2SH version 0x37, got 0x%02x — QBX uses P... not 3... addresses", c.P2SHVersionByte())
	}
}

// TestQBXAddressValidation verifies address decoding for all QBX address types.
func TestQBXAddressValidation(t *testing.T) {
	c := NewQBXCoin()

	tests := []struct {
		name     string
		address  string
		valid    bool
		addrType AddressType
	}{
		// Valid P2PKH — starts with M (version byte 0x32)
		{
			name:     "valid P2PKH M-address (pool address)",
			address:  "MQr2QunAiMQTg4s84cEpm2GSRiRUt4Vn5y",
			valid:    true,
			addrType: AddressTypeP2PKH,
		},
		// Valid P2PKH — from getnewaddress "" pq on real QBX node
		{
			name:     "valid P2PKH M-address (generated by node)",
			address:  "MFSqtVn3HMKZDzdSADTJjqmVNgStP5GKdR",
			valid:    true,
			addrType: AddressTypeP2PKH,
		},
		// pq-prefix addresses cannot be decoded by stratum
		{
			name:    "pq address rejected (cannot decode for coinbase)",
			address: "pqSomePostQuantumAddress123456789abc",
			valid:   false,
		},
		// Bitcoin P2PKH (1...) must NOT be accepted
		{
			name:    "bitcoin 1-address rejected",
			address: "1BvBMSEYstWetqTFn5Au4m4GFg7xJaNVN2",
			valid:   false,
		},
		// Bitcoin P2SH (3...) must NOT be accepted
		{
			name:    "bitcoin 3-address rejected",
			address: "3J98t1WpEZ73CNmQviecrnyiWrnqRhWNLy",
			valid:   false,
		},
		// SegWit bc1q must NOT be accepted
		{
			name:    "segwit bc1q address rejected",
			address: "bc1qar0srrr7xfkvy5l643lydnw9re59gtzzwf5mdq",
			valid:   false,
		},
		// Empty address
		{
			name:    "empty address",
			address: "",
			valid:   false,
		},
	}

	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			err := c.ValidateAddress(tt.address)
			if tt.valid && err != nil {
				t.Errorf("expected valid address, got error: %v", err)
			}
			if !tt.valid && err == nil {
				t.Errorf("expected invalid address to be rejected, but it was accepted")
			}

			if tt.valid {
				_, addrType, decErr := c.DecodeAddress(tt.address)
				if decErr != nil {
					t.Errorf("DecodeAddress failed for valid address: %v", decErr)
				}
				if addrType != tt.addrType {
					t.Errorf("expected address type %v, got %v", tt.addrType, addrType)
				}
			}
		})
	}
}

// TestQBXCoinbaseScriptDilithiumOpcode is the CRITICAL test for the bad-cb-pq fix.
//
// QBX consensus rule (validation.cpp CheckBlock):
//
//	Every coinbase vout[0] MUST use IsPQPayToPubKeyHash() —
//	OP_DUP OP_HASH160 <20b> OP_EQUALVERIFY OP_CHECKSIGDILITHIUM (0xC4).
//
// A standard P2PKH script ending with OP_CHECKSIG (0xAC) causes the
// QBX node to return "bad-cb-pq" and reject the block.
func TestQBXCoinbaseScriptDilithiumOpcode(t *testing.T) {
	c := NewQBXCoin()

	params := CoinbaseParams{
		PoolAddress: "MQr2QunAiMQTg4s84cEpm2GSRiRUt4Vn5y",
	}

	script, err := c.BuildCoinbaseScript(params)
	if err != nil {
		t.Fatalf("BuildCoinbaseScript failed: %v", err)
	}

	// Dilithium P2PKH script must be exactly 25 bytes
	if len(script) != 25 {
		t.Errorf("expected 25-byte Dilithium P2PKH script, got %d bytes", len(script))
	}

	// Byte 0: OP_DUP
	if script[0] != 0x76 {
		t.Errorf("script[0]: expected OP_DUP (0x76), got 0x%02x", script[0])
	}

	// Byte 1: OP_HASH160
	if script[1] != 0xa9 {
		t.Errorf("script[1]: expected OP_HASH160 (0xa9), got 0x%02x", script[1])
	}

	// Byte 2: PUSH 20 bytes
	if script[2] != 0x14 {
		t.Errorf("script[2]: expected PUSH_20 (0x14), got 0x%02x", script[2])
	}

	// Bytes 3-22: 20-byte pubkey hash (from M... address decoding)
	hash := script[3:23]
	if len(hash) != 20 {
		t.Errorf("expected 20-byte hash in script, got %d", len(hash))
	}

	// Byte 23: OP_EQUALVERIFY
	if script[23] != 0x88 {
		t.Errorf("script[23]: expected OP_EQUALVERIFY (0x88), got 0x%02x", script[23])
	}

	// Byte 24: MUST be OP_CHECKSIGDILITHIUM (0xC4), NOT OP_CHECKSIG (0xAC)
	// This is the fix for "bad-cb-pq" — QBX rejects blocks where coinbase
	// vout[0] uses standard OP_CHECKSIG instead of Dilithium variant.
	if script[24] == 0xac {
		t.Fatalf("REGRESSION: script ends with OP_CHECKSIG (0xac) — "+
			"QBX node will reject block with 'bad-cb-pq'. "+
			"Must use OP_CHECKSIGDILITHIUM (0xc4). "+
			"Full script: %s", hex.EncodeToString(script))
	}
	if script[24] != 0xc4 {
		t.Errorf("script[24]: expected OP_CHECKSIGDILITHIUM (0xc4), got 0x%02x — "+
			"QBX requires Dilithium opcode for coinbase vout[0]", script[24])
	}

	t.Logf("Coinbase script OK: %s", hex.EncodeToString(script))
	t.Logf("Final opcode: 0x%02x (OP_CHECKSIGDILITHIUM)", script[24])
}

